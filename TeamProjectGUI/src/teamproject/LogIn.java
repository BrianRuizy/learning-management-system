    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        boolean found = false;
        
        try
        {        
            String username = txtUsername.getText();
            String temp = new String(txtPassword.getPassword());
            int password = Integer.parseInt(temp);
            if(username.equals(admin.uName) && password == admin.getPassword())
            {
            //setVisible(false);
            txtUsername.setText("");
            txtPassword.setText("");
            menuA.setVisible(true);
            found = true;
            }
            else
            {
                for(int i = 0; i < admin.students.size(); i++){
                    if(username.equals(admin.students.get(i).username) && password == admin.students.get(i).getPassword()){
                        //setVisible(false);
                        txtUsername.setText("");
                        txtPassword.setText("");
                        menuS.initialize(admin.students.get(i));
                        menuS.setVisible(true);
                        found = true;
                    }
                }
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Invalid Password");
        }

        
        if(found == false){
            JOptionPane.showMessageDialog(null, "Incorrect username or password");
            txtUsername.setText("");
            txtPassword.setText("");
        }
    }//GEN-LAST:event_btnEnterActionPerformed

   
